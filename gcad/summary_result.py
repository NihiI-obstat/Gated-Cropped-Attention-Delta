#!/usr/bin/env python3
"""Summarize GCAD sweep results.

Aggregates trait expression score and coherence (mean/std) from CSV files
generated by:
  gcad/run.sh
"""

from __future__ import annotations

import argparse
import re
from pathlib import Path

import pandas as pd


CONFIG_PATTERN = re.compile(r"^bc(?P<bc>[-+]?\d*\.?\d+)_sc(?P<sc>[-+]?\d*\.?\d+)$")
PROJECT_ROOT = Path(__file__).resolve().parents[1]


def parse_config_dir(dir_name: str) -> tuple[float, float]:
    match = CONFIG_PATTERN.match(dir_name)
    if match is None:
        raise ValueError(
            f"Invalid config directory name '{dir_name}'. Expected format: bc<coef>_sc<scale>"
        )
    return float(match.group("bc")), float(match.group("sc"))


def _safe_std(values: pd.Series) -> float:
    std = float(values.std(ddof=1))
    return 0.0 if pd.isna(std) else std


def collect_results(results_root: Path) -> pd.DataFrame:
    rows: list[dict] = []
    for csv_path in sorted(results_root.glob("bc*_sc*/*.csv")):
        bc, sc = parse_config_dir(csv_path.parent.name)
        trait = csv_path.stem

        df = pd.read_csv(csv_path)
        if "trait_score" not in df.columns or "coherence" not in df.columns:
            raise ValueError(
                f"Missing required columns in {csv_path}. "
                "Expected: trait_score, coherence"
            )

        if "turn_idx" in df.columns:
            per_turn_df = (
                df.groupby("turn_idx", as_index=False)
                .agg(
                    n=("trait_score", "size"),
                    trait_mean=("trait_score", "mean"),
                    trait_std=("trait_score", _safe_std),
                    coh_mean=("coherence", "mean"),
                    coh_std=("coherence", _safe_std),
                )
                .sort_values("turn_idx")
                .reset_index(drop=True)
            )
            for turn_row in per_turn_df.itertuples(index=False):
                rows.append(
                    {
                        "base_coef": bc,
                        "scale": sc,
                        "trait": trait,
                        "summary_type": "per_turn",
                        "turn": str(int(turn_row.turn_idx)),
                        "n": int(turn_row.n),
                        "trait_mean": float(turn_row.trait_mean),
                        "trait_std": float(turn_row.trait_std),
                        "coh_mean": float(turn_row.coh_mean),
                        "coh_std": float(turn_row.coh_std),
                    }
                )

            rows.append(
                {
                    "base_coef": bc,
                    "scale": sc,
                    "trait": trait,
                    "summary_type": "turn_avg",
                    "turn": "avg",
                    "n": int(per_turn_df["turn_idx"].nunique()),
                    "trait_mean": float(per_turn_df["trait_mean"].mean()),
                    "trait_std": _safe_std(per_turn_df["trait_mean"]),
                    "coh_mean": float(per_turn_df["coh_mean"].mean()),
                    "coh_std": _safe_std(per_turn_df["coh_mean"]),
                }
            )
        else:
            rows.append(
                {
                    "base_coef": bc,
                    "scale": sc,
                    "trait": trait,
                    "summary_type": "turn_avg",
                    "turn": "avg",
                    "n": int(len(df)),
                    "trait_mean": float(df["trait_score"].mean()),
                    "trait_std": _safe_std(df["trait_score"]),
                    "coh_mean": float(df["coherence"].mean()),
                    "coh_std": _safe_std(df["coherence"]),
                }
            )

    if not rows:
        raise FileNotFoundError(
            f"No result CSVs found under {results_root}. "
            "Expected files like bc5.0_sc0.5/evil.csv"
        )

    out = pd.DataFrame(rows)
    turn_order = pd.to_numeric(out["turn"], errors="coerce")
    out["turn_sort"] = turn_order.fillna(10**9)
    return out.sort_values(
        by=["base_coef", "scale", "trait", "summary_type", "turn_sort"]
    ).reset_index(drop=True)


def print_per_trait_table(df: pd.DataFrame) -> None:
    print("\nPer-trait summary")
    print(
        f"{'base_coef':>8}  {'scale':>6}  {'trait':<14}  {'summary':<8}  "
        f"{'turn':>4}  {'n':>4}  "
        f"{'trait_score (mean±std)':>24}  {'coherence (mean±std)':>24}"
    )
    print("-" * 114)
    for row in df.itertuples(index=False):
        print(
            f"{row.base_coef:8.2f}  {row.scale:6.2f}  {row.trait:<14}  "
            f"{row.summary_type:<8}  {row.turn:>4}  {row.n:4d}  "
            f"{row.trait_mean:8.2f}±{row.trait_std:6.2f}  "
            f"{row.coh_mean:8.2f}±{row.coh_std:6.2f}"
        )


def print_per_config_table(df: pd.DataFrame) -> None:
    per_turn = (
        df[df["summary_type"] == "per_turn"]
        .groupby(["base_coef", "scale", "turn"], as_index=False)
        .agg(
            trait_mean=("trait_mean", "mean"),
            trait_std=("trait_mean", "std"),
            coh_mean=("coh_mean", "mean"),
            coh_std=("coh_mean", "std"),
            traits=("trait", "count"),
            total_samples=("n", "sum"),
        )
        .sort_values(["base_coef", "scale", "turn"])
        .reset_index(drop=True)
    )

    avg_over_turns = (
        df[df["summary_type"] == "turn_avg"]
        .groupby(["base_coef", "scale"], as_index=False)
        .agg(
            trait_mean=("trait_mean", "mean"),
            trait_std=("trait_mean", "std"),
            coh_mean=("coh_mean", "mean"),
            coh_std=("coh_mean", "std"),
            traits=("trait", "count"),
            total_samples=("n", "sum"),
        )
        .sort_values(["base_coef", "scale"])
        .reset_index(drop=True)
    )

    if not per_turn.empty:
        print("\nAcross-trait summary per turn (means over traits)")
        print(
            f"{'base_coef':>8}  {'scale':>6}  {'turn':>4}  {'traits':>6}  {'samples':>8}  "
            f"{'trait_score (mean±std)':>24}  {'coherence (mean±std)':>24}"
        )
        print("-" * 103)
        for row in per_turn.itertuples(index=False):
            trait_std = 0.0 if pd.isna(row.trait_std) else row.trait_std
            coh_std = 0.0 if pd.isna(row.coh_std) else row.coh_std
            print(
                f"{row.base_coef:8.2f}  {row.scale:6.2f}  {row.turn:>4}  {row.traits:6d}  {row.total_samples:8d}  "
                f"{row.trait_mean:8.2f}±{trait_std:6.2f}  "
                f"{row.coh_mean:8.2f}±{coh_std:6.2f}"
            )

    print("\nAcross-trait summary (turn average; means over traits)")
    print(
        f"{'base_coef':>8}  {'scale':>6}  {'traits':>6}  {'turns':>8}  "
        f"{'trait_score (mean±std)':>24}  {'coherence (mean±std)':>24}"
    )
    print("-" * 96)
    for row in avg_over_turns.itertuples(index=False):
        trait_std = 0.0 if pd.isna(row.trait_std) else row.trait_std
        coh_std = 0.0 if pd.isna(row.coh_std) else row.coh_std
        print(
            f"{row.base_coef:8.2f}  {row.scale:6.2f}  {row.traits:6d}  {row.total_samples:8d}  "
            f"{row.trait_mean:8.2f}±{trait_std:6.2f}  "
            f"{row.coh_mean:8.2f}±{coh_std:6.2f}"
        )


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description=(
            "Aggregate trait expression score and coherence from "
            "gated prompt-attn-delta result CSVs."
        )
    )
    parser.add_argument(
        "--results_root",
        type=Path,
        default=PROJECT_ROOT
        / "results/Qwen2.5-7B-Instruct/gcad_L9_10_11_12_13_14_15_16_17_18_19_turn_10",
        help=(
            "Root directory containing bc*_sc*/<trait>.csv "
            "(default: %(default)s)"
        ),
    )
    return parser


def main() -> None:
    args = build_parser().parse_args()
    results_root = args.results_root.resolve()
    print(f"Reading results from: {results_root}")

    df = collect_results(results_root)
    print_per_trait_table(df)
    print_per_config_table(df)


if __name__ == "__main__":
    main()
